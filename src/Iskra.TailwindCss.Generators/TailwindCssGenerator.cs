using System;
using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;
using System.Threading;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace Iskra.TailwindCss.Generators;

[Generator]
public class TailwindCssGenerator : IIncrementalGenerator
{
    private static readonly DiagnosticDescriptor CompilationError = new(
        id: "TWCSS001",
        title: "Tailwind CSS compilation failed",
        messageFormat: "Tailwind CSS compilation failed: {0}",
        category: "TailwindCss",
        defaultSeverity: DiagnosticSeverity.Error,
        isEnabledByDefault: true);

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        context.RegisterPostInitializationOutput(static ctx =>
        {
            ctx.AddSource("GeneratedTailwindCssAttribute.g.cs", SourceText.From("""
                // <auto-generated/>
                #nullable enable

                namespace Iskra.TailwindCss;

                [System.AttributeUsage(System.AttributeTargets.Method)]
                public sealed class GeneratedTailwindCssAttribute : System.Attribute
                {
                    public GeneratedTailwindCssAttribute(string css, params string[] pairs) { }
                }
                """, Encoding.UTF8));
        });

        // 1. Collect all string literals across all source files, deduplicate, sort, join
        var candidatesProvider = context.SyntaxProvider
            .CreateSyntaxProvider(
                predicate: static (node, _) => node is LiteralExpressionSyntax literal
                    && literal.IsKind(SyntaxKind.StringLiteralExpression),
                transform: static (ctx, _) =>
                    ((LiteralExpressionSyntax)ctx.Node).Token.ValueText)
            .Collect()
            .Select(static (strings, _) =>
            {
                var candidates = strings
                    .Where(static s => !string.IsNullOrWhiteSpace(s))
                    .SelectMany(static s => s.Split(new[] { ' ', '\t', '\n', '\r' },
                        StringSplitOptions.RemoveEmptyEntries))
                    .Distinct()
                    .OrderBy(static s => s)
                    .ToImmutableArray();
                // Join to single string for value equality in incremental pipeline
                return string.Join("\n", candidates);
            });

        // 2. Find methods annotated with [GeneratedTailwindCss]
        var methodProvider = context.SyntaxProvider
            .ForAttributeWithMetadataName(
                "Iskra.TailwindCss.GeneratedTailwindCssAttribute",
                predicate: static (node, _) => node is MethodDeclarationSyntax,
                transform: static (ctx, ct) => GetMethodInfo(ctx, ct))
            .Where(static info => info is not null);

        // 3. Read the runtimes directory from MSBuild property
        var runtimesDirProvider = context.AnalyzerConfigOptionsProvider
            .Select(static (options, _) =>
            {
                options.GlobalOptions.TryGetValue(
                    "build_property.TailwindCssGeneratorRuntimesDir", out var dir);
                return dir ?? "";
            });

        // 4. Combine each attributed method with the full candidate list and runtimes dir
        var combined = methodProvider
            .Combine(candidatesProvider)
            .Combine(runtimesDirProvider);

        // 5. Generate source
        context.RegisterSourceOutput(combined,
            static (spc, source) => Execute(spc, source.Left.Left!, source.Left.Right, source.Right));
    }

    private record MethodInfo(
        string MethodName,
        string ContainingNamespace,
        string ContainingTypeName,
        string Accessibility,
        bool IsStatic,
        string Css,
        ImmutableArray<string> Pairs);

    private static MethodInfo? GetMethodInfo(
        GeneratorAttributeSyntaxContext ctx, CancellationToken ct)
    {
        if (ctx.TargetNode is not MethodDeclarationSyntax methodDecl)
            return null;

        if (ctx.TargetSymbol is not IMethodSymbol methodSymbol)
            return null;

        var attr = ctx.Attributes.FirstOrDefault();
        if (attr is null)
            return null;

        // First constructor arg: css string
        var css = attr.ConstructorArguments[0].Value as string ?? "";

        // Second constructor arg: params string[] pairs
        var pairsArg = attr.ConstructorArguments[1];
        var pairs = pairsArg.Values
            .Select(static v => v.Value as string ?? "")
            .ToImmutableArray();

        var containingType = methodSymbol.ContainingType;
        var ns = containingType.ContainingNamespace.IsGlobalNamespace
            ? ""
            : containingType.ContainingNamespace.ToDisplayString();

        var accessibility = methodSymbol.DeclaredAccessibility switch
        {
            Accessibility.Public => "public",
            Accessibility.Internal => "internal",
            Accessibility.Private => "private",
            Accessibility.Protected => "protected",
            Accessibility.ProtectedOrInternal => "protected internal",
            Accessibility.ProtectedAndInternal => "private protected",
            _ => "public"
        };

        return new MethodInfo(
            MethodName: methodSymbol.Name,
            ContainingNamespace: ns,
            ContainingTypeName: containingType.Name,
            Accessibility: accessibility,
            IsStatic: methodSymbol.IsStatic,
            Css: css,
            Pairs: pairs);
    }

    private static void Execute(SourceProductionContext spc, MethodInfo info, string candidatesJoined, string runtimesDir)
    {
        var candidates = candidatesJoined.Split(new[] { '\n' }, StringSplitOptions.RemoveEmptyEntries);

        // Build import map from pairs [id, content, id, content, ...]
        var importMap = new Dictionary<string, string>();
        for (int i = 0; i + 1 < info.Pairs.Length; i += 2)
        {
            importMap[info.Pairs[i]] = info.Pairs[i + 1];
        }

        string generatedCss;
        try
        {
            generatedCss = TailwindCompiler.BuildCss(info.Css, importMap, candidates, runtimesDir);
        }
        catch (Exception ex)
        {
            spc.ReportDiagnostic(Diagnostic.Create(CompilationError, Location.None, ex.Message));
            return;
        }

        var staticModifier = info.IsStatic ? "static " : "";

        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();

        if (!string.IsNullOrEmpty(info.ContainingNamespace))
        {
            sb.AppendLine($"namespace {info.ContainingNamespace};");
            sb.AppendLine();
        }

        sb.AppendLine($"partial class {info.ContainingTypeName}");
        sb.AppendLine("{");
        sb.AppendLine($"    {info.Accessibility} {staticModifier}partial string {info.MethodName}() =>");
        sb.AppendLine("\"\"\"");
        sb.Append(generatedCss);
        sb.AppendLine("\"\"\";");
        sb.AppendLine("}");

        var hintName = string.IsNullOrEmpty(info.ContainingNamespace)
            ? $"{info.ContainingTypeName}.{info.MethodName}.g.cs"
            : $"{info.ContainingNamespace}.{info.ContainingTypeName}.{info.MethodName}.g.cs";

        spc.AddSource(hintName, SourceText.From(sb.ToString(), Encoding.UTF8));
    }
}
