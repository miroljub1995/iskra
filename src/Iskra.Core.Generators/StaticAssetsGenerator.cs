using System.Collections.Generic;
using System.Collections.Immutable;
using System.IO;
using System.Text;
using System.Text.Json;
using System.Text.RegularExpressions;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace Iskra.Core.Generators;

[Generator]
public class StaticAssetsGenerator : IIncrementalGenerator
{
    internal const string ManifestFileName = "staticwebassets.build.json";
    private const string AttributeFullName = "Iskra.Core.GeneratedStaticAssetPathsAttribute";

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        context.RegisterPostInitializationOutput(static ctx =>
        {
            ctx.AddSource("GeneratedStaticAssetPathsAttribute.g.cs", SourceText.From("""
                // <auto-generated/>
                #nullable enable

                namespace Iskra.Core;

                [System.AttributeUsage(System.AttributeTargets.Class)]
                public sealed class GeneratedStaticAssetPathsAttribute : System.Attribute { }
                """, Encoding.UTF8));
        });

        var classProvider = context.SyntaxProvider
            .ForAttributeWithMetadataName(
                AttributeFullName,
                predicate: static (node, _) => node is ClassDeclarationSyntax,
                transform: static (ctx, _) =>
                {
                    var symbol = (INamedTypeSymbol)ctx.TargetSymbol;
                    var ns = symbol.ContainingNamespace.IsGlobalNamespace
                        ? null
                        : symbol.ContainingNamespace.ToDisplayString();
                    return (ClassName: symbol.Name, Namespace: ns);
                });

        var manifestProvider = context.AdditionalTextsProvider
            .Where(static file => Path.GetFileName(file.Path) == ManifestFileName)
            .Select(static (file, ct) => file.GetText(ct)?.ToString())
            .Collect();

        var assemblyName = context.CompilationProvider
            .Select(static (c, _) => c.AssemblyName);

        var combined = classProvider
            .Combine(manifestProvider)
            .Combine(assemblyName);

        context.RegisterSourceOutput(combined, static (spc, pair) =>
        {
            var ((classInfo, manifests), asmName) = pair;
            if (asmName is null)
                return;

            var json = manifests.IsDefaultOrEmpty ? null : manifests[0];
            if (json is null)
                return;

            var source = Generate(json, asmName, classInfo.ClassName, classInfo.Namespace);
            if (source is not null)
                spc.AddSource($"{classInfo.ClassName}.g.cs", SourceText.From(source, Encoding.UTF8));
        });
    }

    internal static string? Generate(string manifestJson, string sourceId, string className, string? namespaceName)
    {
        var assets = ParseDiscoveredAssets(manifestJson, sourceId);
        if (assets.Count == 0)
            return null;

        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");

        if (namespaceName is not null)
        {
            sb.Append("namespace ").Append(namespaceName).AppendLine(";");
            sb.AppendLine();
        }

        sb.Append("public static partial class ").AppendLine(className);
        sb.AppendLine("{");

        foreach (var asset in assets)
        {
            var propertyName = BuildPropertyName(asset.CleanPath);
            if (string.IsNullOrEmpty(propertyName))
                continue;

            sb.Append("    public static string ").Append(propertyName)
              .Append(" => \"/").Append(asset.ResolvedPath).AppendLine("\";");
        }

        sb.AppendLine("}");
        return sb.ToString();
    }

    internal static List<AssetInfo> ParseDiscoveredAssets(string json, string sourceId)
    {
        var results = new List<AssetInfo>();

        using var doc = JsonDocument.Parse(json);
        var root = doc.RootElement;

        if (!root.TryGetProperty("Assets", out var assetsArray))
            return results;

        foreach (var asset in assetsArray.EnumerateArray())
        {
            var sourceType = asset.GetProperty("SourceType").GetString();
            var assetSourceId = asset.GetProperty("SourceId").GetString();

            if (assetSourceId != sourceId)
                continue;

            var relativePath = asset.GetProperty("RelativePath").GetString();
            if (relativePath == null)
                continue;

            var isDiscovered = sourceType == "Discovered";
            var isDotnetJs = sourceType == "Computed"
                && Regex.IsMatch(relativePath, @"^_framework/dotnet#.*\.js$");

            if (!isDiscovered && !isDotnetJs)
                continue;

            var fingerprint = asset.GetProperty("Fingerprint").GetString();

            var resolved = ResolveFingerprint(relativePath, fingerprint);
            var clean = StripFingerprintExpression(relativePath);
            results.Add(new AssetInfo(resolved, clean));
        }

        return results;
    }

    internal static string ResolveFingerprint(string relativePath, string? fingerprint)
    {
        if (string.IsNullOrEmpty(fingerprint) || !relativePath.Contains("#["))
            return relativePath;

        return Regex.Replace(relativePath, @"#\[([^\]]*)\](?:(!)(.)|\?)", m =>
        {
            var expr = m.Groups[1].Value;
            var resolved = expr.Replace("{fingerprint}", fingerprint);
            if (m.Groups[2].Success)
                return resolved + m.Groups[3].Value;
            return resolved;
        });
    }

    internal static string StripFingerprintExpression(string relativePath)
    {
        return Regex.Replace(relativePath, @"#\[[^\]]*\][!?]", "");
    }

    internal static string BuildPropertyName(string cleanPath)
    {
        var fileName = Path.GetFileName(cleanPath) ?? cleanPath;
        var directory = Path.GetDirectoryName(cleanPath)?.Replace('\\', '/');

        var segments = new List<string>();
        if (!string.IsNullOrEmpty(directory))
            segments.AddRange(directory!.Split('/'));
        segments.Add(fileName);

        var parts = new List<string>();
        foreach (var segment in segments)
        {
            if (string.IsNullOrEmpty(segment)) continue;
            var words = Regex.Split(segment, @"[^a-zA-Z0-9]+");
            foreach (var word in words)
            {
                if (word.Length == 0) continue;
                parts.Add(char.ToUpperInvariant(word[0])
                    + (word.Length > 1 ? word.Substring(1).ToLowerInvariant() : ""));
            }
        }

        return string.Join("_", parts);
    }

    internal readonly struct AssetInfo
    {
        public string ResolvedPath { get; }
        public string CleanPath { get; }

        public AssetInfo(string resolvedPath, string cleanPath)
        {
            ResolvedPath = resolvedPath;
            CleanPath = cleanPath;
        }
    }
}
