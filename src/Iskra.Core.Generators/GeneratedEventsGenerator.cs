using System.Collections.Immutable;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace Iskra.Core.Generators;

[Generator]
public class GeneratedEventsGenerator : IIncrementalGenerator
{
    private static readonly DiagnosticDescriptor NonVoidMethodError = new(
        id: "ISKRA001",
        title: "Event method must return void",
        messageFormat: "Event method '{0}' must have a void return type",
        category: "GeneratedEvents",
        defaultSeverity: DiagnosticSeverity.Error,
        isEnabledByDefault: true);

    private static readonly SymbolDisplayFormat FullyQualifiedNullableFormat =
        SymbolDisplayFormat.FullyQualifiedFormat.WithMiscellaneousOptions(
            SymbolDisplayFormat.FullyQualifiedFormat.MiscellaneousOptions |
            SymbolDisplayMiscellaneousOptions.IncludeNullableReferenceTypeModifier);

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        context.RegisterPostInitializationOutput(static ctx =>
        {
            ctx.AddSource("GeneratedEventsAttribute.g.cs", SourceText.From("""
                // <auto-generated/>
                #nullable enable

                namespace Iskra.Core.Components;

                [System.AttributeUsage(System.AttributeTargets.Class)]
                public sealed class GeneratedEventsAttribute : System.Attribute { }
                """, Encoding.UTF8));
        });

        var classInfos = context.SyntaxProvider
            .ForAttributeWithMetadataName(
                "Iskra.Core.Components.GeneratedEventsAttribute",
                predicate: static (node, _) => node is ClassDeclarationSyntax,
                transform: static (ctx, ct) => GetClassInfo(ctx, ct))
            .Where(static info => info is not null);

        context.RegisterSourceOutput(classInfos, static (spc, info) => Execute(spc, info!));
    }

    private static ClassInfo? GetClassInfo(GeneratorAttributeSyntaxContext ctx, CancellationToken ct)
    {
        if (ctx.TargetNode is not ClassDeclarationSyntax classDecl)
            return null;

        if (ctx.TargetSymbol is not INamedTypeSymbol classSymbol)
            return null;

        var methods = new List<MethodInfo>();

        foreach (var member in classDecl.Members)
        {
            ct.ThrowIfCancellationRequested();

            if (member is not MethodDeclarationSyntax methodDecl)
                continue;

            if (!methodDecl.Modifiers.Any(m => m.IsKind(SyntaxKind.PartialKeyword)))
                continue;

            // Only declaration stubs (no body)
            if (methodDecl.Body is not null || methodDecl.ExpressionBody is not null)
                continue;

            var methodSymbol = ctx.SemanticModel
                .GetDeclaredSymbol(methodDecl, ct) as IMethodSymbol;

            var parameters = methodDecl.ParameterList.Parameters
                .Select((p, i) =>
                {
                    var typeSymbol = methodSymbol?.Parameters.ElementAtOrDefault(i)?.Type;
                    var typeName = typeSymbol?.ToDisplayString(FullyQualifiedNullableFormat)
                        ?? p.Type?.ToString()
                        ?? "object";
                    return new ParameterInfo(typeName, p.Identifier.Text);
                })
                .ToImmutableArray();

            bool isVoid = methodSymbol?.ReturnsVoid ?? true;

            // Store location as comparable primitives (avoid non-equatable Location in data model)
            var lineSpan = methodDecl.Identifier.GetLocation().GetLineSpan();
            var methodLocation = new MethodLocation(
                lineSpan.Path,
                lineSpan.StartLinePosition.Line,
                lineSpan.StartLinePosition.Character,
                lineSpan.EndLinePosition.Line,
                lineSpan.EndLinePosition.Character);

            methods.Add(new MethodInfo(methodDecl.Identifier.Text, isVoid, parameters, methodLocation));
        }

        var namespaceName = classSymbol.ContainingNamespace.IsGlobalNamespace
            ? null
            : classSymbol.ContainingNamespace.ToDisplayString();

        return new ClassInfo(classSymbol.Name, namespaceName, methods.ToImmutableArray());
    }

    private static void Execute(SourceProductionContext spc, ClassInfo info)
    {
        // Report errors for non-void methods
        foreach (var method in info.Methods)
        {
            if (!method.IsVoid)
            {
                var loc = method.MethodLocation;
                var location = loc is not null
                    ? Location.Create(loc.FilePath, default, new LinePositionSpan(
                        new LinePosition(loc.StartLine, loc.StartCharacter),
                        new LinePosition(loc.EndLine, loc.EndCharacter)))
                    : Location.None;

                spc.ReportDiagnostic(Diagnostic.Create(NonVoidMethodError, location, method.Name));
            }
        }

        var validMethods = info.Methods.Where(m => m.IsVoid).ToList();

        var sb = new StringBuilder();
        sb.AppendLine("""
            // <auto-generated/>
            #nullable enable
            """);

        if (info.Namespace is not null)
        {
            sb.AppendLine($$"""
                namespace {{info.Namespace}};
                """);
        }

        sb.AppendLine($$"""
            public partial class {{info.ClassName}} : global::Iskra.Core.Components.BaseEmits
            {
            """);

        foreach (var method in validMethods)
        {
            var actionType = BuildActionType(method.Parameters);
            var paramList = string.Join(", ", method.Parameters.Select(p => $"{p.TypeName} {p.ParameterName}"));
            var invoke = method.Parameters.IsEmpty
                ? $"On{method.Name}?.Invoke();"
                : $"On{method.Name}?.Invoke({string.Join(", ", method.Parameters.Select(p => p.ParameterName))});";

            sb.AppendLine($$"""
                        public {{actionType}}? On{{method.Name}} { get; init; } = null;

                        public partial void {{method.Name}}({{paramList}})
                        {
                            if (Disabled) return;
                            {{invoke}}
                        }
                    """);
        }

        sb.AppendLine("""
            }
            """);

        var hintName = info.Namespace is not null
            ? $"{info.Namespace}.{info.ClassName}.g.cs"
            : $"{info.ClassName}.g.cs";

        spc.AddSource(hintName, SourceText.From(sb.ToString(), Encoding.UTF8));
    }

    private static string BuildActionType(ImmutableArray<ParameterInfo> parameters)
    {
        if (parameters.IsEmpty)
            return "global::System.Action";

        var typeArgs = string.Join(", ", parameters.Select(p => p.TypeName));
        return $"global::System.Action<{typeArgs}>";
    }
}

internal sealed record MethodLocation(
    string FilePath,
    int StartLine,
    int StartCharacter,
    int EndLine,
    int EndCharacter);

internal sealed record ClassInfo(
    string ClassName,
    string? Namespace,
    ImmutableArray<MethodInfo> Methods);

internal sealed record MethodInfo(
    string Name,
    bool IsVoid,
    ImmutableArray<ParameterInfo> Parameters,
    MethodLocation? MethodLocation);

internal sealed record ParameterInfo(
    string TypeName,
    string ParameterName);
