using Iskra.WebIDLGenerator.Extensions;
using Iskra.WebIDLGenerator.Models;

namespace Iskra.WebIDLGenerator.Generators;

public class InterfaceGenerator(
    IDLInterfaceMemberTypeGenerator idlInterfaceMemberTypeGenerator
)
{
    public string Generate(InterfaceType input, string ns)
    {
        var baseTypeName = input.Inheritance ?? "JSObjectProxy";

        List<string> bodyParts = [];

        foreach (var idlInterfaceMemberType in input.Members)
        {
            var part = idlInterfaceMemberTypeGenerator.Generate(idlInterfaceMemberType);
            if (!string.IsNullOrEmpty(part))
            {
                bodyParts.Add(part);
            }
        }

        var body = string.Join("\n\n", bodyParts);

        var content = $$"""
                        // <auto-generated/>

                        using System.Runtime.InteropServices.JavaScript;
                        using Iskra.JSCore;

                        namespace {{ns}};

                        #nullable enable

                        public partial class {{input.Name}}(JSObject obj): {{baseTypeName}}(obj)
                        {
                        {{body.IndentLines(4)}}
                        }

                        #nullable disable
                        """;

        return content;
    }
}